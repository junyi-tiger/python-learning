# 本文件展示了安装第三方库的方法

# 1. pip安装:命令行操作
# 最主要的安装方法，适合99%以上情况
# 需要联网，受限于网络条件
# pip install <第三方库名> : 安装第三方库
# pip insatll -U <第三方库名> : 更新第三方库
# pip uninstall <第三方库名> : 卸载第三方库
# pip download <第三方库名> : 下载但不安装
# pip show <第三方库名> : 列出某个指定的第三方库的详细信息
# pip search <关键词> : 根据关键词在库的名称和介绍中检索
# pip list : 列出已经安装的第三方库

# 2. 文件安装
# 有些第三方库可以用pip下载，但不能直接安装，需要编译再安装
# 如果操作系统没有编译环境，则能下载但不能安装
# 可以直接下载编译后的版本进行安装
